// Add your code below:
var swap = 0

let length = unsortedIntegers.count
var arr = unsortedIntegers

for index in 0 ..< length  {
    var pass = 0
    let current = arr[index]
    var previous = index - 1

    while (previous >= 0 && current < arr[previous]) {
        arr[previous + 1]  = arr[previous]
        arr[previous] = current
        swap += 1
        pass += 1
        previous -= 1
    }
    
    print("Pass: \(index), Swaps: \(pass)/\(swap), Array: \(arr)")
}


